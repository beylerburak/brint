type EmojiCategory = keyof typeof EMOJI_CATEGORIES;

export interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  trigger?: React.ReactNode;
  align?: "start" | "center" | "end";
  side?: "top" | "right" | "bottom" | "left";
}

/**
 * Emoji Picker Component
 * 
 * A reusable emoji picker with categories, search, and grid layout.
 * Can be used across different pages and domains.
 */
export function EmojiPicker({
  onEmojiSelect,
  trigger,
  align = "start",
  side = "bottom",
}: EmojiPickerProps) {
  const [search, setSearch] = React.useState("");
  const [selectedCategory, setSelectedCategory] = React.useState<EmojiCategory>("smileys");
  const [recentEmojis, setRecentEmojis] = React.useState<string[]>([]);

  // Load recent emojis from localStorage
  React.useEffect(() => {
    const stored = localStorage.getItem("emoji-picker-recent");
    if (stored) {
      try {
        setRecentEmojis(JSON.parse(stored));
      } catch {
        // Ignore parse errors
      }
    }
  }, []);

  // Save emoji to recent when selected
  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    
    // Add to recent (max 20)
    setRecentEmojis((prev) => {
      const filtered = prev.filter((e) => e !== emoji);
      const updated = [emoji, ...filtered].slice(0, 20);
      localStorage.setItem("emoji-picker-recent", JSON.stringify(updated));
      return updated;
    });
  };

  // Filter emojis by search
  const filteredEmojis = React.useMemo(() => {
    // Get emojis for current category
    const currentEmojis =
      selectedCategory === "recent"
        ? recentEmojis.length > 0
          ? recentEmojis
          : []
        : EMOJI_CATEGORIES[selectedCategory].emojis;

    if (!search) return currentEmojis;

    // Simple search - could be enhanced with emoji names
    return currentEmojis.filter((emoji) => emoji.includes(search));
  }, [search, selectedCategory, recentEmojis]);

  const defaultTrigger = (
    <Button
      type="button"
      variant="ghost"
      size="sm"
      className="h-8 w-8 p-0"
    >
      <Smile className="h-4 w-4" />
    </Button>
  );

  return (
    <Popover>
      <PopoverTrigger asChild>
        {trigger || defaultTrigger}
      </PopoverTrigger>
      <PopoverContent
        className="w-auto p-0"
        align={align}
        side={side}
      >
        <div className="flex flex-col">
          {/* Category Icons Bar */}
          <div className="flex items-center border-b px-2 py-2 gap-1">
            {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => {
              const Icon = category.icon;
              const isSelected = selectedCategory === key;
              return (
                <button
                  key={key}
                  type="button"
                  onClick={() => {
                    setSelectedCategory(key as EmojiCategory);
                    setSearch("");
                  }}
                  className={cn(
                    "flex h-10 w-8 items-center justify-center rounded transition-colors",
                    isSelected
                      ? "bg-primary text-primary-foreground"
                      : "text-muted-foreground hover:bg-accent hover:text-foreground"
                  )}
                  title={category.name}
                >
                  <Icon className="h-5 w-5" />
                </button>
              );
            })}
          </div>

          {/* Search Bar */}
          <div className="border-b px-2 py-2">
            <div className="relative">
              <Search className="absolute left-2 top-1/2 h-4 w-4 -translate-y-1/2 text-muted-foreground" />
              <Input
                placeholder="Ara"
                value={search}
                onChange={(e) => setSearch(e.target.value)}
                className="pl-8 h-8 w-full"
              />
            </div>
          </div>

          {/* Category Name */}
          <div className="border-b px-2 py-1.5">
            <p className="text-xs font-medium text-muted-foreground">
              {EMOJI_CATEGORIES[selectedCategory].name}
            </p>
          </div>

          {/* Emoji Grid - This determines the dialog width */}
          <div className="overflow-y-auto" style={{ maxHeight: "300px" }}>
            {filteredEmojis.length > 0 ? (
              <div className="grid grid-cols-8 gap-x-1 gap-y-1">
                {filteredEmojis.map((emoji, index) => (
                  <button
                    key={`${emoji}-${index}`}
                    type="button"
                    onClick={() => handleEmojiSelect(emoji)}
                    className="flex h-10 w-10 items-center justify-center rounded hover:bg-accent text-2xl transition-colors"
                    title={emoji}
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            ) : (
              <div className="text-center py-8 px-2 text-sm text-muted-foreground">
                {selectedCategory === "recent" && recentEmojis.length === 0
                  ? "HenÃ¼z emoji kullanÄ±lmadÄ±"
                  : "Emoji bulunamadÄ±"}
              </div>
            )}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  );
}

